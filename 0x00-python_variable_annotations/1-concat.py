#!/usr/bin/env python3
"""Basic annotations
"""


def concat(str1: str, str2: str) -> str:
    """Function that concatenates two string
    and returns the concatenated string

    Args:
        str1 (str): first string
        str2 (str): second string

    Returns:
        str: concatenation of str1 + str2
    """
    return str1 + str2
